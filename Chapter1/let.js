//let으로 선언한 변수는 값 변경이 가능
//변수 myName에 '이현수'대입
let myName = '이현수';

//myName값을 alert()로 출력
alert(myName);

//숫자형 변수의 덧셈
let number1 = 10;
let number2 = 20;
let sum = number1 + number2;
alert(sum);

//문자형 변수의 덧셈
let firstName = '이';
let familyName = "현수";
let fullName = firstName + familyName;
alert(fullName);

//다른 변수에 대입
let ar = 10;
let vr = ar;
alert(vr);

//변수 let은 초기화는 생략이 가능