//배열 정의!!!

const array1 = []; // 빈 배열
console.log(array1);

const array2 = [0, 2, 8] //'0', '2', '8'이 들어있는 데이터 타입
console.log(array2);

const array3 = ['곰', '여우'] //'곰', '여우'가 들어있는 데이터 타입
console.log(array3);

const array4 =[1, '곰' , false]; //'1', '곰', 'false'가 들어있는 데이터 타입
console.log(array4);

const array5 = [[1, 1, 1], [2, 2, 2]]; //[1, 1, 1], [2, 2, 2]가 들어있는 데이터 타입
console.log(array5);

const array6 = [{ id: 1, name: '곰'}, { id: 2, name: '고양이'}]; //두 개의 객체가 들어있는 데이터 타입
console.log(array6);

const array7 = ['곰', '여우', '고양이'];
console.log(array7[0]);
console.log(array7[1]);
console.log(array7[2]);

const array8 = new Array('곰', '여우');
console.log(array8[0]);
console.log(array8[1]);

const array9 = new Array(10); //10개의 데이터를 넣을 수 있음
console.log(array9[0]);